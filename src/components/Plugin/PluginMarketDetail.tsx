/**
 * The five tabs of the detail dialog. The order here is the order
 * the tabs render in; the strip is generated by mapping over this
 * tuple so adding / re-ordering is a one-line change.
 */
type DetailTab = 'overview' | 'changelog' | 'permissions' | 'dependencies' | 'reviews'

/**
 * Reload the plugin registry after a successful install / update /
 * rollback. Centralised so every install path (latest version,
 * historical version, rollback) follows the same post-write dance:
 * scan → loadAllPlugins → setPlugins. Without this shared helper
 * each install branch would carry its own copy and any future
 * change to the reload path (e.g. a new failure reporting hook)
 * would have to be made in three places.
 */
async function reloadAfterInstall(): Promise<void> {
  try {
    const list = await scanPlugins()
    const { plugins: defs, failures } = await loadAllPlugins(list)
    usePluginStore.getState().setPlugins(defs)
    // G2: forward any load failures the rescan surfaced so the
    // banner stays consistent with what's on disk.
    usePluginStore.getState().setLoadFailures(failures)
  } catch (e) {
    console.warn('post-install reload failed', e)
  }
}

export function PluginMarketDetail({
  entry,
  index,
  localVersion,
  onClose,
  onInstalled,
}: {
  entry: PluginIndexEntry
  index: PluginIndex
  localVersion: string | null
  onClose: () => void
  /**
   * Called by the dialog right before closing, after a successful
   * install / update / rollback. The parent (`PluginMarketView`)
   * uses the returned `PluginMetadataRust` to open the post-install
   * permission grant dialog — the same affordance the user-upload
   * path in `PluginManagerView` provides. Without this callback the
   * marketplace install would silently skip the permission step and
   * the user would have to discover the per-card "Permissions"
   * button before they could grant the first permission.
   *
   * Only the metadata is forwarded (id, name, etc.) — we don't pass
   * the full `PluginDefinition` because the registry might not have
   * picked the new entry up yet by the time the callback runs.
   */
  onInstalled?: (meta: PluginMetadataRust) => void
}) {
  const { t } = useTranslation()
  const refreshUpdates = usePluginMarketStore((s) => s.refreshUpdates)

  const [activeTab, setActiveTab] = useState<DetailTab>('overview')
  const [versions, setVersions] = useState<PluginVersionInfo[]>([])
  const [isInstalling, setIsInstalling] = useState(false)
  // Tracks which specific version the user is installing from the
  // changelog tab — keyed by the version string so two version rows
  // can be in-flight independently (and the per-row spinner lights
  // up only on the row that was clicked, not the whole tab).
  const [installingVersion, setInstallingVersion] = useState<string | null>(null)
  const [isRolling, setIsRolling] = useState<string | null>(null)
  const [error, setError] = useState<string | null>(null)

  // Load the list of locally-installed versions so the rollback UI
  // is populated before the user clicks anything.
  useEffect(() => {
    let cancelled = false
    void (async () => {
      if (localVersion == null) {
        setVersions([])
        return
      }
      try {
        const v = await listPluginVersions(entry.id)
        if (!cancelled) setVersions(v)
      } catch {
        if (!cancelled) setVersions([])
      }
    })()
    return () => {
      cancelled = true
    }
  }, [entry.id, localVersion])

  // ── Dependency resolution (G4) ─────────────────────────────────────────────
  // The marketplace wire format carries dependencies as a
  // `string[]` of `"<id>@<range>"` entries. We feed the
  // `usePluginStore`'s installed plugins into the resolver so
  // the user sees, in real time, which deps are missing,
  // present-but-wrong, or part of a cycle. The resolver is
  // pure (no I/O), so the `useMemo` is cheap and re-runs only
  // when the index entry, the installed list, or the index
  // map change.
  const installedPluginsMap = usePluginStore((s) => s.plugins)
  const indexMap = useMemo<Record<string, ResolverIndexEntry>>(() => {
    const m: Record<string, ResolverIndexEntry> = {}
    for (const e of index.plugins) m[e.id] = { id: e.id, version: e.version }
    return m
  }, [index])
  const installedMap = useMemo<Record<string, ResolverInstalledPlugin>>(() => {
    const m: Record<string, ResolverInstalledPlugin> = {}
    for (const p of installedPluginsMap) {
      m[p.id] = { version: p.version, dependencies: p.dependencies }
    }
    return m
  }, [installedPluginsMap])
  const dependencyResolution: DependencyResolution = useMemo(
    () =>
      resolveDependencies(
        {
          id: entry.id,
          version: entry.version,
          dependencies: parseDependencyList(entry.dependencies),
        },
        installedMap,
        indexMap,
      ),
    [entry.id, entry.version, entry.dependencies, installedMap, indexMap],
  )
  // True when the install / update button must be disabled:
  // there are unsatisfied versions (can't be auto-fixed) or
  // detected cycles. Missing deps *do* still let the install
  // proceed if the user resolves them via the auto-resolve
  // button — so the button is only disabled for *hard*
  // failures.
  const hasBlockingDependencyIssue =
    dependencyResolution.unsatisfied.length > 0 ||
    dependencyResolution.cycles.length > 0
  // Whether the auto-resolve button can fire: there's at least
  // one missing dep whose `available` slot is filled by the
  // marketplace index. Missing deps with no index entry are
  // un-installable from the marketplace, so the button is
  // hidden.
  const canAutoResolve = dependencyResolution.missing.some(
    (m) => m.available.length > 0,
  )
  const [isAutoResolving, setIsAutoResolving] = useState(false)

  // The locally-installed plugin copy (if any). Used by the
  // Permissions tab to show the actual declared permissions
  // for this id — the marketplace index itself doesn't carry
  // a permissions list, so without the installed copy we'd
  // always have to render the "unknown" empty state.
  const installedPlugin = usePluginStore((s) =>
    s.plugins.find((p) => p.id === entry.id) ?? null,
  )
  const declaredPermissions: PluginPermission[] = installedPlugin?.permissions ?? []

  /**
   * Install one plugin entry from the marketplace index. Lifted
   * out of `onInstall` so the auto-resolve flow can install
   * missing dependencies without re-implementing the
   * download → install → reload → toast chain. Returns the
   * installed metadata on success, or throws on failure (so
   * callers can short-circuit the chain).
   */
  const installEntry = async (
    e: PluginIndexEntry
  ): Promise<PluginMetadataRust | null> => {
    const bytes = await downloadPluginZip(e)
    const pubkeyB64 = effectivePubkey(index, e)
    return installPluginFromBytes({
      pluginId: e.id,
      version: e.version,
      bytes,
      sha256: e.sha256,
      pubkeyB64,
      signatureB64: e.signatureB64,
    })
  }

  const onInstall = async () => {
    // Early guard against double-clicks landing in the same render
    // cycle. The button's `disabled` prop already covers the slow
    // case (state has flipped to `isInstalling` by the time React
    // re-renders) but a synchronous double-click before any state
    // update commits would otherwise queue two installs and
    // produce two success toasts for the same plugin.
    if (isInstalling || installingVersion !== null) return
    setError(null)
    // Re-check at click time: the user might have updated the
    // store via the install manager tab between the resolution
    // memo and this click. If something is still off (hard
    // failures only — missing deps the user hasn't yet
    // resolved), refuse and surface the same banner the dialog
    // already shows.
    if (hasBlockingDependencyIssue) {
      const reason =
        dependencyResolution.cycles.length > 0
          ? t('plugin.market.depsCycleDetected', {
              defaultValue: '检测到循环依赖，无法安装',
            })
          : t('plugin.market.depsUnsatisfied', {
              defaultValue: '依赖版本不满足，无法安装',
            })
      setError(reason)
      return
    }
    setIsInstalling(true)
    let installedMeta: PluginMetadataRust | null = null
    try {
      installedMeta = await installEntry(entry)
      // The host successfully wrote the plugin. Run the full reload
      // path (scan → loadAllPlugins → setPlugins) so the new entry
      // is registered with the store *and* gets its `onLoad` hook
      // fired by the host-takeover layer. A shortcut like
      // `setPlugins(list as any)` would skip `loadAllPlugins` and
      // the onLoad hook would never run, leaving the plugin's
      // event subscriptions / storage seeding silent.
      await reloadAfterInstall()
      await refreshUpdates()
      toast.success(
        t('plugin.market.installed', {
          defaultValue: '已安装 {{name}} v{{version}}',
          name: entry.name,
          version: entry.version,
        })
      )
      // Hand off to the parent so the post-install permission
      // dialog can open with the freshly-loaded plugin's declared
      // permission list. The parent closes this dialog and opens
      // the permission modal in a follow-up tick so the two
      // dialogs don't stack.
      if (onInstalled && installedMeta) {
        onInstalled(installedMeta)
      } else {
        onClose()
      }
    } catch (e: any) {
      setError(e?.message ?? String(e))
    } finally {
      setIsInstalling(false)
    }
  }

  /**
   * Auto-resolve missing dependencies by installing each one
   * that's available in the marketplace index. The flow is
   * deliberately simple: one pass through the *immediate*
   * missing list. Transitive dependencies (a missing dep
   * whose own deps are also missing) require a second click
   * after the first round of installs lands; surfacing a
   * recursive resolver here would muddle the UX with hidden
   * state and potential permission dialogs. We do bail early
   * on the first failure so a single broken download doesn't
   * hold up the queue.
   *
   * After the chain succeeds we still leave the original
   * "install" decision to the user — auto-resolve only stages
   * the dependencies. The next click of "Install" runs the
   * full pre-install check again, and the resolution banner
   * either vanishes (deps satisfied) or surfaces any
   * remaining issues.
   */
  const onAutoResolve = async () => {
    setError(null)
    if (!canAutoResolve || isAutoResolving) return
    setIsAutoResolving(true)
    let succeeded = 0
    let failed = 0
    try {
      for (const missing of dependencyResolution.missing) {
        if (!missing.available) continue
        const depEntry = index.plugins.find((e) => e.id === missing.id) ?? null
        if (!depEntry) {
          // Marketplace no longer carries this id (race vs
          // index refresh). Skip rather than block.
          failed++
          continue
        }
        try {
          await installEntry(depEntry)
          succeeded++
        } catch {
          failed++
        }
      }
      await reloadAfterInstall()
      await refreshUpdates()
      if (succeeded > 0) {
        toast.success(
          t('plugin.market.depsAutoResolved', {
            defaultValue: '已自动解决 {{count}} 个依赖',
            count: succeeded,
          }),
        )
      }
      if (failed > 0) {
        toast.error(
          t('plugin.market.depsAutoResolveFailed', {
            defaultValue: '{{count}} 个依赖安装失败',
            count: failed,
          }),
        )
      }
    } catch (e: any) {
      setError(e?.message ?? String(e))
    } finally {
      setIsAutoResolving(false)
    }
  }

  /**
   * Install a specific historical version (Task 5 / G5). The
   * version comes from the `versions` array on the index entry —
   * the host receives the explicit `version` string so the on-disk
   * `.versions/<v>/` directory is named correctly. The SHA-256 we
   * pass is the per-version digest (G1 reuse), not the latest
   * entry-level digest, and the signature is the entry-level one
   * (the index protocol attaches one signature per `PluginIndexEntry`,
   * not per version).
   */
  const onInstallVersion = async (version: PluginIndexEntryVersion) => {
    // Same early guard as `onInstall`: don't queue a second per-version
    // install while the first one is still in flight, and don't
    // race against a top-level install. `version.version` is the
    // natural idempotency key for the per-version entry point.
    if (isInstalling || installingVersion !== null || isRolling !== null) {
      return
    }
    setError(null)
    setInstallingVersion(version.version)
    let installedMeta: PluginMetadataRust | null = null
    try {
      const bytes = await downloadPluginVersion(entry.id, version)
      const pubkeyB64 = effectivePubkey(index, entry)
      installedMeta = await installPluginFromBytes({
        pluginId: entry.id,
        version: version.version,
        bytes,
        sha256: version.sha256,
        pubkeyB64,
        signatureB64: entry.signatureB64,
      })
      await reloadAfterInstall()
      await refreshUpdates()
      toast.success(
        t('plugin.market.installedVersion', {
          defaultValue: '已安装 {{name}} v{{version}}',
          name: entry.name,
          version: version.version,
        })
      )
      if (onInstalled && installedMeta) {
        onInstalled(installedMeta)
      } else {
        onClose()
      }
    } catch (e: any) {
      setError(e?.message ?? String(e))
    } finally {
      setInstallingVersion(null)
    }
  }

  const onRollback = async (version: string) => {
    // Rollback must not race with another install or another
    // rollback. The row's `disabled` prop already covers the slow
    // case; this catches the synchronous double-click window
    // (React hasn't re-rendered yet) and prevents the UI from
    // showing two "Rollback in progress" badges.
    if (isInstalling || installingVersion !== null || isRolling !== null) {
      return
    }
    setError(null)
    setIsRolling(version)
    try {
      await rollbackPlugin(entry.id, version)
      // Same full-reload path as install. Rolling back to a previous
      // version is conceptually a fresh plugin load, so we want
      // onLoad/onUnload to fire cleanly.
      await reloadAfterInstall()
      await refreshUpdates()
      toast.success(
        t('plugin.market.rolledBack', {
          defaultValue: '已回滚到 {{version}}',
          version,
        })
      )
      // A rollback is a re-activation of an already-installed
      // plugin whose permissions were set on the original install.
      // We don't re-open the permission dialog here — the user
      // has already made their decision on the prior install, and
      // re-prompting on every rollback would be a regression.
      // (The post-install path is purely a "first install" affordance.)
      onClose()
    } catch (e: any) {
      setError(e?.message ?? String(e))
    } finally {
      setIsRolling(null)
    }
  }

  const isInstalled = localVersion != null
  const isUpdateAvailable = isInstalled && localVersion !== entry.version
  // `entry.versions` is the wire-format list (newest first). Some
  // older indexes may not ship it; the existing "is at least one
  // version listed" check guards both the tab visibility and the
  // per-row rendering below.
  const hasVersionHistory = entry.versions.length > 0

  // Stable count of declared dependencies, used to badge the
  // Dependencies tab in the strip so the user can tell at a
  // glance whether the plugin has any peer-plugin requirements.
  const declaredDepsCount = parseDependencyList(entry.dependencies).length

  return (
    <Dialog open onOpenChange={(o) => !o && onClose()}>
      <DialogContent style={{ maxWidth: 600, maxHeight: '85vh', display: 'flex', flexDirection: 'column' }}>
        <DialogHeader>
          <DialogTitle style={{ display: 'flex', alignItems: 'center', gap: 8 }}>
            <Package size={18} />
            {entry.name || t('plugin.market.unknownName', { defaultValue: '未命名插件' })}
            <Badge variant="outline" style={{ marginLeft: 4 }}>
              v{entry.version}
            </Badge>
            {isUpdateAvailable && (
              <Badge style={{ background: 'var(--accent, #4f46e5)', color: 'white' }}>
                <Download size={10} />
                {t('plugin.market.badgeUpdate', { defaultValue: 'Update' })}
              </Badge>
            )}
            {isInstalled && !isUpdateAvailable && (
              <Badge variant="secondary">
                <CheckCircle2 size={10} />
                {t('plugin.market.badgeInstalled', { defaultValue: 'Installed' })}
              </Badge>
            )}
          </DialogTitle>
          <DialogDescription style={{ fontFamily: 'var(--font-mono, monospace)', fontSize: 11 }}>
            {entry.id}
          </DialogDescription>
        </DialogHeader>

        {/*
          Tab strip. Always visible so the chrome doesn't pop in/out
          as the user installs / uninstalls the plugin (which can
          change `localVersion` mid-session). Each tab carries an
          icon + label and the active state is a 2px accent border
          (mirroring common settings / marketplace tab strips). The
          count badges (deps / permissions) hint at content without
          forcing the user to switch tabs to find out.
        */}
        <div
          role="tablist"
          className="pmd-tabstrip"
          style={{
            display: 'flex',
            gap: 0,
            borderBottom: '1px solid var(--border, #e5e5e5)',
            marginBottom: 4,
            flexWrap: 'wrap',
          }}
        >
          <DetailTabButton
            label={t('plugin.market.tabOverview', { defaultValue: '基本信息' })}
            icon={<Info size={12} />}
            active={activeTab === 'overview'}
            onClick={() => setActiveTab('overview')}
            data-testid="pmd-tab-overview"
          />
          <DetailTabButton
            label={t('plugin.market.tabChangelog', { defaultValue: '更新日志' })}
            icon={<FileText size={12} />}
            active={activeTab === 'changelog'}
            onClick={() => setActiveTab('changelog')}
            data-testid="pmd-tab-changelog"
          />
          <DetailTabButton
            label={t('plugin.market.tabPermissions', { defaultValue: '权限' })}
            icon={<Shield size={12} />}
            active={activeTab === 'permissions'}
            onClick={() => setActiveTab('permissions')}
            badge={isInstalled ? declaredPermissions.length : undefined}
            data-testid="pmd-tab-permissions"
          />
          <DetailTabButton
            label={t('plugin.market.tabDependencies', { defaultValue: '依赖' })}
            icon={<Network size={12} />}
            active={activeTab === 'dependencies'}
            onClick={() => setActiveTab('dependencies')}
            badge={declaredDepsCount > 0 ? declaredDepsCount : undefined}
            data-testid="pmd-tab-dependencies"
          />
          <DetailTabButton
            label={t('plugin.market.tabReviews', { defaultValue: '评价' })}
            icon={<Star size={12} />}
            active={activeTab === 'reviews'}
            onClick={() => setActiveTab('reviews')}
            data-testid="pmd-tab-reviews"
          />
        </div>

        <ScrollArea style={{ flex: 1, paddingRight: 8 }}>
          <div
            className="pmd-tabpanel"
            style={{ display: 'flex', flexDirection: 'column', gap: 16, padding: '4px 0' }}
          >
            {/*
              "基本信息" tab — name + id + author + tags + description.
              The dependency summary that used to live here has been
              moved to its own tab (Task 14 / G14). Nothing else
              semantically changed beyond the tab switcher above.
            */}
            {activeTab === 'overview' && (
              <div className="pmd-overview">
                <OverviewPanel
                  entry={entry}
                  localVersion={localVersion}
                  isInstalled={isInstalled}
                  isUpdateAvailable={isUpdateAvailable}
                />
              </div>
            )}

            {/*
              "更新日志" tab (G14 / was G5 "历史版本") — per-version
              release notes (newest first) plus the local versions
              with rollback actions. Lifted out of the old combined
              "overview + history" pair so the changelog text gets
              the room it deserves.
            */}
            {activeTab === 'changelog' && (
              <div className="pmd-changelog">
                <ChangelogPanel
                  entry={entry}
                  hasVersionHistory={hasVersionHistory}
                  versions={versions}
                  isInstalled={isInstalled}
                  localVersion={localVersion}
                  installingVersion={installingVersion}
                  isInstalling={isInstalling}
                  isRolling={isRolling}
                  onInstallVersion={onInstallVersion}
                  onRollback={onRollback}
                />
              </div>
            )}

            {/*
              "权限" tab — shows the permissions the installed copy
              of the plugin declared. The marketplace index doesn't
              ship a permission list (only the on-disk manifest
              does), so for not-yet-installed plugins we render a
              centred empty state explaining why.
            */}
            {activeTab === 'permissions' && (
              <div className="pmd-permissions">
                <PermissionsPanel
                  isInstalled={isInstalled}
                  declaredPermissions={declaredPermissions}
                />
              </div>
            )}

            {/*
              "依赖" tab (G4) — full dependency resolution list,
              lifted out of the previous overview sub-section. The
              per-row icon + label communicates the same verdict
              (✓ satisfied, ⚠ version mismatch, ✗ missing, ⛔
              cycle) but in a dedicated panel.
            */}
            {activeTab === 'dependencies' && (
              <div className="pmd-dependencies">
                <DependenciesPanel
                  entry={entry}
                  dependencyResolution={dependencyResolution}
                  hasBlockingDependencyIssue={hasBlockingDependencyIssue}
                />
              </div>
            )}

            {/*
              "评价" tab — currently a no-content placeholder.
              Reviews aren't a built-in feature of the marketplace
              protocol yet, so we render a centred empty state
              with a "coming soon" hint instead of removing the
              tab from the strip (so the chrome doesn't change
              when the feature lands).
            */}
            {activeTab === 'reviews' && (
              <div className="pmd-reviews">
                <EmptyStatePanel
                  icon={<Star size={28} />}
                  title={t('plugin.market.noReviews', { defaultValue: '暂无评价' })}
                  hint={t('plugin.market.noReviewsHint', {
                    defaultValue: '社区评价系统即将上线。',
                  })}
                />
              </div>
            )}

            {error && (
              <div
                style={{
                  display: 'flex',
                  alignItems: 'flex-start',
                  gap: 6,
                  padding: 8,
                  background: 'rgba(192, 57, 43, 0.08)',
                  border: '1px solid rgba(192, 57, 43, 0.3)',
                  borderRadius: 4,
                  color: 'var(--danger, #c0392b)',
                  fontSize: 12,
                }}
              >
                <AlertCircle size={14} style={{ flexShrink: 0, marginTop: 1 }} />
                <span>{error}</span>
              </div>
            )}
          </div>
        </ScrollArea>

        <DialogFooter style={{ display: 'flex', gap: 8 }}>
          <Button variant="outline" onClick={onClose} disabled={isInstalling || installingVersion !== null || isRolling !== null || isAutoResolving}>
            <X size={14} />
            {t('common.close', { defaultValue: '关闭' })}
          </Button>
          {/* "自动解决依赖" affordance. Only shown when there's
              at least one missing dep that's also present in
              the marketplace index (i.e. we know how to
              install it). The button runs a sequential
              install for each missing dep; the user clicks
              "Install" again afterwards to retry the parent
              plugin. */}
          {canAutoResolve && (
            <Button
              variant="outline"
              onClick={onAutoResolve}
              disabled={isAutoResolving || isInstalling}
              data-testid="plugin-auto-resolve"
            >
              {isAutoResolving ? (
                <Loader2 size={14} className="animate-spin" />
              ) : (
                <Wand2 size={14} />
              )}
              {t('plugin.market.autoResolve', {
                defaultValue: '自动解决依赖',
              })}
            </Button>
          )}
          {!isInstalled && (
            <Button
              onClick={onInstall}
              disabled={isInstalling || installingVersion !== null || hasBlockingDependencyIssue}
              data-testid="plugin-install"
            >
              {isInstalling ? <Loader2 size={14} className="animate-spin" /> : <Download size={14} />}
              {t('plugin.market.install', { defaultValue: '安装' })}
            </Button>
          )}
          {isUpdateAvailable && (
            <Button
              onClick={onInstall}
              disabled={isInstalling || installingVersion !== null || hasBlockingDependencyIssue}
            >
              {isInstalling ? <Loader2 size={14} className="animate-spin" /> : <Download size={14} />}
              {t('plugin.market.updateTo', {
                defaultValue: '更新到 v{{version}}',
                version: entry.version,
              })}
            </Button>
          )}
          {isInstalled && !isUpdateAvailable && (
            <Button variant="outline" disabled>
              <CheckCircle2 size={14} />
              {t('plugin.market.upToDate', { defaultValue: '已是最新' })}
            </Button>
          )}
        </DialogFooter>
      </DialogContent>
    </Dialog>
  )
}

